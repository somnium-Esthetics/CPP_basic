//ch01ex_두수합.cpp

#include <iostream>

using namespace std;

int main()
{
	int n1, n2;
	cout << "두 정수 입력:";
	cin >> n1 >> n2;
	cout << "두 정수 합:" << n1 + n2 << endl;

	//두 실수의 합
	double f1, f2;
	cout << "두 실수 입력:";
	cin >> f1 >> f2;
	cout << "두 실수 합:" << f1 + f2 << endl;

	return 0;
}